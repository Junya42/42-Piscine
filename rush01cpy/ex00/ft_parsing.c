#include "sky.h"

int	ft_parsing(char *str)
{
	int i;
	int j;
	
	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == 32)
			j++;
		if (str[i % 2] < '0' && str[i % 2] > '4')
			return (0);
		i++;
	}
	if (j != 15)
		return (0);
	return (1);
}
