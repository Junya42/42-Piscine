int	plus(int x, int y)
{
	return (x + y);
}

int	moins(int x, int y)
{
	return (x - y);
}

int	multip(int x, int y)
{
	return (x * y);
}

int	div(int x, int y)
{
	return (x / y);
}

int	mod(int x, int y)
{
	return (x % y);
}
